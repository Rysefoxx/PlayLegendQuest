package io.github.rysefoxx.scoreboard.enums;

/**
 * @author Rysefoxx
 * @since 18.05.2024
 */
public enum ScoreboardPredefinedValue {

    /**
     * Fill the placeholder in the scoreboard with the quest name.
     */
    QUEST_NAME,
    /**
     * Fills the placeholder in the scoreboard with the remaining time.
     */
    REMAINING_TIME,
    /**
     * Fill the placeholder in the scoreboard with the quest progress.
     */
    PROGRESS

}
